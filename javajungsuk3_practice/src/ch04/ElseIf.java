package ch04;

class ElseIf {
    public static void main(String[] args) {

        for(int i=0; i<=10; i++){
            if(i==0) System.out.println(0);
            else if(i==1) System.out.println(1);
            else if(i==2) System.out.println(2);
            else if(i==3) System.out.println(3);
            else System.out.println(3333);
        }
    }
}
